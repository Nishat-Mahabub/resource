void solve(){
    ll n, m, k, H;
    cin >> n >> m >> k >> H;
    ll ans = 0;
    for (ll i = 0; i < n;i++){
        ll data;
        cin >> data;
        ll x = abs(data - H);
        if(x!=0 && x%k==0 && x/k<m){
            ans++;
        }
    }
    cout << ans << endl;
}
